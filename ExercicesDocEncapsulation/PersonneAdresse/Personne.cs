using System;
using System.Collections.Generic;
using System.Text;

namespace PersonneAdresse
{
    class Personne
    {
        private string prenom;
        private string nom;
        private int age;
        private Adresse adresse;

        public Personne(string _prenom, string _nom, int _age, Adresse _adresse)
        {
            this.prenom = _prenom;
            this.nom = _nom;
            this.age = _age;
            this.adresse = _adresse;
            this.adresse = _adresse;
        }

    }
}
